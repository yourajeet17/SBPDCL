                            <strong>What is Slab calculation? ( An Example)</strong>
                        </div>



                        <div class="col-md-11">
                            <p><span><span>The calculation of slab is done on the
                                        basis of actual number of days in the bill
                                        month.</span></span></p>

                            <p><span><span>If the billing period is 31 days and
                                        covers 26 days of June and 05 days of July
                                        respectively with total consumption of 448
                                        units, please find the slab wise calculation
                                        for reference: </span></span><br>
                                <b><span><span><span>Calculation of First
                                                Slab:</span></span></span></b>&nbsp;&nbsp;&nbsp;<br>
                                <span><span>Calculation for units of
                                        June:</span></span>
                            </p>

                            <p><span><span>200 (First slab units) / 30 (No. of days
                                        in June) X 26 (No. of days billed for June)
                                        &nbsp;&nbsp;&nbsp;= 173.33 (units entitled
                                        for first slab)</span></span><br>
                                <span><span>Calculation for units of July:
                                    </span></span>
                            </p>

                            <p><span><span>200 (First slab units) / 31 (No. of days
                                        in July) X 05 (No. of days billed for July)
                                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;= 32.25
                                        (units entitled for first
                                        slab)</span></span><br>
                                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span><span>
                                    </span></span><span><span>
                                    </span></span><span><span>
                                    </span></span><span><span>
                                    </span></span><span><span> </span></span><br>
                                <span><span>&nbsp;&nbsp;Total units entitled for
                                        First Slab = 205.58 Units (Round off 206
                                        Units) </span></span>
                            </p>

                            <p>&nbsp;</p>

                            <p><b><span><span><span>Calculation of Second Slab:
                                                &nbsp;</span></span></span></b></p>

                            <p><span><span>Calculation for units of
                                        June:</span></span></p>

                            <p><span><span>200 (Second slab units) / 30 (No. of days
                                        in June) X 26 (No. of days billed for June)
                                        &nbsp;&nbsp;&nbsp;= 173.33 (units entitled
                                        for Second slab)</span></span><br>
                                <span><span>Calculation for units of July:
                                    </span></span>
                            </p>

                            <p><span><span>200 (Second slab units) / 31 (No. of days
                                        in July) X 05 (No. of days billed for July)
                                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;= 32.25
                                        (units entitled for first
                                        slab)</span></span><br>
                                <span><span>Total units entitled for Second Slab =
                                        205.58 Units (Round off 205
                                        Units)</span></span>
                            </p>

                            <p>&nbsp;<b><span><span><span>Calculation of Third Slab:
                                                &nbsp;&nbsp;</span></span></span></b>
                            </p>

                            <p><span><span>448 (Total units consumed) – ((206 (First
                                        Slab Units) + 205 (Second Slab Units)) = 37
                                        Units</span></span></p>
