                            <strong>What is the role of "N cut" and what action
                                consumer should take?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>The "N cut" LED (which is provided in few meters)
                                indicates a loose wire at the meter end. In case
                                consumer finds such LED glowing he should call the
                                service company for retightening of all the wires in
                                the meter to ensure proper supply to meter.</p>
