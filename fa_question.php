                        <div class="col-md-11">
                            <strong>What is an Energy Meter?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>An energy meter is a device to measure the
                                consumption of energy.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>How is the accuracy of a meter defined?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>As per IS 137779/ IEC61036/ ID13010 accuracy of a
                                meter is defined by comparing the difference of
                                energy consumption recorded by "Meter under test"
                                and of the "Reference Meter".<br>
                                Accuracy (%) = (EMUT - ERef) x 100 (E Ref)<br>
                                The accuracy of both Mechanical and Electronic meter
                                is defined by above equation.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>How are meters classified and what are the
                                relevant technologies?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Meters are classified as following:-<br>
                                Household single phase meter - Mechanical/
                                Electronic meter<br>
                                Household three phase meter - Mechanical/ Electronic
                                meter&nbsp;<br>
                                Commercial Meter - Electronic meter<br>
                                Industrial Meter - Electronic meter&nbsp;<br>
                                HT Meter - Electronic meter&nbsp;<br>
                                Grid Meter - Electronic meter<br>
                                Reference Meter - Electronic meter</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What are the various ratings of meter and how
                                are they defined?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Various ratings of meter are as following:-</p>

                            <p>Ratings&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                                &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
                                &nbsp; &nbsp; &nbsp; &nbsp;Range*</p>

                            <p>1. 2.5-5A (Mechanical)&nbsp; &nbsp; &nbsp;225mA-5Amp
                            </p>

                            <p>2. 5A-10A (Mechanical)&nbsp; &nbsp;50mA-10Amp</p>

                            <p>3.&nbsp; 5A-20A (Mechanical)&nbsp; &nbsp;50mA-20A</p>

                            <p>4.&nbsp; 10-40A (Mechanical)&nbsp; &nbsp; 100mA-40A
                            </p>

                            <p>5.&nbsp; 10-60A (Electronic)&nbsp;</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What is the effect of aging on energy
                                consumption recording?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Since the mechanical meters have moving parts, so
                                with use these have the tendency of wear and tear
                                resulting in low energy consumption. Same is not
                                applicable for electronic meter as it is a static
                                device.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>Meters accuracy are defined at reference
                                conditions. However, in field voltage, Frequency and
                                Temperature are different. What is the effect of the
                                same?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>The permissible effect of field voltage, frequency
                                and temperature on accuracy is defined in IS/ CBIP/
                                IEC standards. All energy meters in India, in
                                general follow these standards.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What are the responsibilities of ISI license
                                holder?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>After evaluating the product and the manufacturing
                                capabilities, the Bureau of Indian Standard (BIS)
                                issues the ISI license to the manufacturer. Meter
                                manufacturer has to ensure all meters strictly
                                follow the relevant Indian Standards and test
                                records of the same are maintained. The
                                misconception that the service company can ask
                                manufacturer to make meters with very high positive
                                error (fast meter) is not possible as if it is found
                                so, the ISI license of the manufacturer can be
                                withdrawn by BIS.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What is the Maximum Demand Indicator (MDI)? What
                                is its significance for consumer?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Maximum Demand Indicator is an indication about the
                                maximum load used by the consumer for the duration
                                of half-an-hour in the given period. This parameter
                                is significant both for consumer and the service
                                company to decide the sanctioned load and also to
                                plan the network capacity.<br>
                                Kindly note - Exceeding the sanctioned load attracts
                                penalty.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What is the meaning of down loading of
                                parameter?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Various parameters measured and recorded by the
                                instrument are finally downloaded for billing/
                                monitoring purpose. The downloading of parameter
                                means transferring the parameters from meter to the
                                records of the service company. Downloading can be
                                manual i.e. by reading the LCD display recording on
                                a notebook or using some gadgets.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What is the meaning of a MRI reading?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>The gadget used for downloading data is called Meter
                                Reading Instrument (MRI). The biggest advantage of
                                MRI reading is that it avoids human error in
                                recording/ transfer of data.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What is an AMR system?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Downloading of the parameters using electronic
                                gadgets which are attached to the meter without
                                manual intervention is called Automatic Meter
                                Reading (AMR) system. The gadget attached to the
                                meter downloads the parameter and then automatically
                                communicates it to the computer of the service
                                company.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What is EL LED? What does it signify if it
                                glows?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>The full form of EL LED is Earth Leakage Light
                                Emitting Diode. An EL LED indicator is available on
                                all electronic meters. If this EL LED glows it
                                indicates an unequal current flowing through the
                                phase and neutral wires. This mismatch can be either
                                due to shortening of neural to earth or leakage of
                                current to earth or that the wire of two premises
                                are touching each other.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What action should a consumer take if the EL-
                                LED glows?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>In case the EL LED is found glowing, the consumer
                                should get his wiring checked for the above
                                mentioned defects.&nbsp;<a href="/web/brpl/initiative-to-train-electricians">Click
                                    here for RWA nominated electricians trained by
                                    BSES to check internal house wiring.</a></p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What is the significance of REV LED?</strong>
                        </div>

                            <p>The REV LED indicator lights up to show the reverse
                                flow of energy from consumer to the grid.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
                        <div class="col-md-11">
                            <strong>What action consumer should take if REV LED
                                glows?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>In case REV LED is glowing then the consumer should
                                ensure that generator/ inverter is totally isolated
                                from the grid. Ever after that if the REV LED is
                                found ON the consumer should call the service
                                company for a check.<br>
                                Kindly note the glowing of REV LED does not affect
                                the accuracy of energy meters. Sometimes "REV" LED
                                glows at "no load" condition and can thus be
                                ignored.</p>
                        </div>
                        <div class="col-md-1">
                             
                        </div>
