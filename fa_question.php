                        <div class="col-md-11">
                            <strong>What is meant by the term 'Phases'?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Alternating Current (AC) electricity changes its
                                direction of flow in a regular, cyclic manner.
                                Because electrical current flows in response to an
                                applied voltage, the voltage of the AC supply must
                                also have been changing polarity from positive to
                                negative and back again at the same frequency as the
                                alternating current.</p>

                            <p>The distribution line supplying your home may be
                                single phase and have only two wires strung between
                                the poles (we will use the overhead power lines as
                                examples because they can be easily seen). However,
                                the distribution line may be made up of 4 lines.
                                What are the others? The other lines carry the
                                currents from two other electrical circuits, making
                                a total of three circuits. Because these circuits
                                are electrically linked (see below), they are called
                                phases. The reason why there are only 4 lines is
                                because the 3 phases have a common neutral line
                                (i.e. 3 active lines and 1 common neutral line).</p>

                            <p>In this "low voltage" part of the distribution
                                system, the voltage between the active and neutral
                                wires is 240 volts. The neutral wire is kept at the
                                same electrical potential as the earth, so that the
                                voltage between the active and earth is also 240
                                volts. The voltage between the phases in the low
                                voltage distribution system is 415 volts (in
                                Australia). A 415 volt 3-phase supply is able to
                                deliver more energy than a 240 volt single phase
                                supply. A 3-phase supply to a home would normally be
                                required only for large electrical loads. 3-phase
                                supplies are common in industrial areas and shopping
                                centers.</p>
                        </div>
                        
                        <div class="col-md-11">
                            <strong>What is ‘Earthing’, and how do safety switches
                                work?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>The power points in your home have three sockets.
                                Some of your appliances have three pin plugs while
                                other appliances have only two pin plugs. Why? The
                                answer lies in the concept of "Earthing".</p>

                            <p>The lower two sockets in a power point are connected
                                to the active and neutral wires. The top socket is
                                connected to a separate wire which is "earthed"
                                (connected to the earth). The need for a separate
                                earth wire can be explained by considering your
                                toaster. A toaster usually has a metal enclosure.
                                Because metals are electrical conductors (i.e. allow
                                electricity to pass through them), this metal
                                enclosure is "earthed" so that, if the active wire
                                came in contact with the metal enclosure, the
                                electricity would pass to earth. An appliance that
                                requires its enclosure to be earthed must therefore
                                have a three pin plug (active, neutral and earth).
                            </p>

                            <p>An increasing number of appliances are enclosed in
                                materials that prevent the flow of electricity
                                through them (i.e. they are electrical insulators).
                                Because these insulated enclosures do not need to be
                                earthed, the appliances may have plugs with only two
                                pins (active and neutral).</p>

                            <p>If, for any reason, the active wire comes in contact
                                with the earth wire, the electrical current (flow of
                                electricity) passing through the active wire to
                                earth through the earth wire could be large enough
                                to activate the overload protection device in the
                                active wire's circuit.</p>

                            <p>A circuit is the term used to describe an active wire
                                that can be isolated from within your home's
                                switchboard. For example, your electric hot water
                                system and your electric stove usually have their
                                own circuits. Your power points may all be connected
                                to the one "power" circuit or they could be divided
                                into two or more separate power circuits. Your
                                lights could also be supplied from one or more
                                "lighting" circuits. Each circuit in your home
                                should be protected against overload by a device
                                that senses the current passing through the active
                                wire of the circuit and isolates the circuit when
                                the current is too high. The overload protection
                                device could be a fuse (a special type of wire that
                                melts if the current passing through it is more that
                                its rated current). More usually now, the overload
                                protection device could be a type of switch that
                                operates on current overload.</p>

                            <p>We saw above that the overload protection device in a
                                circuit could operate if the active wire contacted
                                the earth wire. A current overload could also occur
                                if the active wire came into direct contact with the
                                neutral wire.</p>

                            <p>Let us now look at what happens when you become part
                                of an electric circuit. If you contact an active
                                wire and you are electrically connected to the
                                earth, current will pass through you to earth. If
                                your connection to earth is poor (e.g. you are
                                standing on a carpet or a wood chair), you may be
                                lucky enough to escape with only a mild shock. If
                                not, the size and duration of the electric current
                                passing through you to ground may have more drastic
                                consequences! A current could also flow through you
                                if you contact both the active and neutral wires.
                            </p>

                            <p>In both these cases, the current passing through you
                                may be high enough to activate the circuit's
                                overload protection device and turn off the supply
                                to that circuit - but by the time that happened, it
                                may have been too late so save you. What is needed
                                is a device that would sense that something is wrong
                                and switch off the supply of electricity before you
                                are injured.</p>

                            <p>One such device is the "safety switch". This device
                                is also called an "earth leakage circuit breaker"
                                because of the way it operates. To understand how it
                                operates, we need to realise that, in a normal
                                circuit, the current flowing in the active wire of
                                the circuit is exactly the same as the current in
                                the neutral wire of the circuit. If a fault occurs
                                in the circuit and some current flows to earth, the
                                current in the neutral wire would be less than the
                                current in the active wire. A safety switch senses
                                this imbalance in currents and isolates the circuit
                                if the imbalance becomes greater than a preset
                                value. Because safety switches can sense and react
                                to this type of situation much quicker (and at a
                                smaller current) than a normal overload protection
                                device, severe electrical shocks and electrocutions
                                are prevented.</p>

                            <p>However, it must be realised that safety switches
                                cannot protect you if you come in contact with both
                                the active and neutral wires because in this case
                                current does not flow to earth and there is no
                                imbalance in the active and neutral currents.</p>
                        </div>
