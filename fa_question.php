                        </div>



                        <div class="col-md-11">
                            <p><span><span>An assessment bill shall be raised for
                                        the period for which the defective/burnt
                                        meter remained on site, based on the
                                        estimated consumption by taking the
                                        consumption pattern of the consumer for the
                                        past 12 months prior to the period during
                                        which the meter remained defective. Where
                                        the recorded consumption of past 12 months
                                        is not available, the next twelve months
                                        consumption pattern of new meter would be
                                        considered for raising the assessment
                                        bill.</span></span></p>
