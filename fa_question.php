                            <strong>What is the Meaning of terms Volts, Watts and
                                Kilowatt Hours?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>The correct use and meaning of many electrical terms
                                have, in many cases, become unclear through general
                                use. These explanations attempt to show the correct
                                usage of these terms.</p>

                            <p>Electrical Terminology - VOLTS, WATTS and KILOWATT
                                HOURS<br>
                                1. That the electricity supplied to your home is at
                                240 volts;<br>
                                2. That transmission lines (the ones using large
                                steel towers) operate at "high voltage";<br>
                                3. That you use light bulbs of different wattage
                                (e.g. 75 watt or 60 watt) in your home;<br>
                                4. That when you purchase a new electrical heater,
                                one of your considerations is its wattage (e.g.
                                1,000 watts); and That when you pay your quarterly
                                electricity bill, you are paying for the
                                kilowatt-hours of electricity you have used in your
                                home during that period.</p>

                            <p>These are the electrical terms most frequently used,
                                both at work and at home. For example you may (or
                                may not) know: Electrical voltage can be thought of
                                as a measure of the electrical "pressure" applied to
                                the electrical system to force the electricity to
                                flow through the wires. A commonly used analogy is
                                the water supply to your home where the water
                                pressure forces the water through the pipes.</p>

                            <p>Voltage is measured in volts (V). For convenience,
                                higher voltages are identified in kilovolts (kV)
                                where 1 kV = 1,000 V.</p>

                            <p>In the "high voltage" parts of the electrical supply
                                network, voltages of 11 kV, 33 kV, 66 kV, 110 kV and
                                275 kV are common.</p>

                            <p>In the "low voltage" part of the electricity supply
                                network, your home is supplied with 240 V
                                electricity and, if you have a large air
                                conditioner, with 415 V electricity. (More
                                correctly, these should be called a 240 V single
                                phase supply and a 415 V 3-phase supply. We will
                                talk about "phases" in a later discussion.)</p>

                            <p>Electrical wattage is a harder concept to visualise
                                because it is, essentially, a measure of how fast
                                electricity is being used - more correctly the "rate
                                of use of electrical energy". For example, a 2,000 W
                                electrical heater would use electrical energy twice
                                as fast as a 1,000 W heater. For convenience, the
                                term kilowatt (kW) is often used instead of 1,000
                                watts. The wattage of the new 1,000 W electrical
                                heater mentioned above therefore could be identified
                                as 1 kW.</p>

                            <p>At the other end of the electricity supply system,
                                power stations are producing electricity to match
                                the rate at which electricity is consumed by the end
                                users (plus losses). The "rate at which electrical
                                energy can be produced" determines the wattage of a
                                power station. Usually, power stations are rated in
                                terms of kilowatts (kW) or megawatts (MW) where 1 MW
                                = 1,000 kW.</p>

                            <p>Electrical energy is commonly measured in terms of
                                kilowatt-hours (kWh) here 1 kWh = 1,000 watt-hours.
                                As a simple example, the 1 kW electrical heater
                                mentioned above would use 1 kWh of electrical energy
                                during each hour it is switched on (i.e. electrical
                                energy used in 1 hour = 1 kW x 1 hour = 1 kWh).</p>

                            <p>Large amounts of electrical energy are measured in
                                terms of megawatt-hours (MWh) where 1 MWh = 1,000
                                kWh, or gigawatt-hours (GWh) where 1 GWh = 1,000
                                MWh.</p>

                            <p>Note: The "rate of use of electrical energy" (kW) in
                                your home is continually varying. The meter in the
                                switchboard of your home is designed to overcome
                                this variability in its recording of your
                                consumption of electrical energy (kWh). Your
                                electricity account uses data from this meter to
                                identify the amount of electrical energy (kWh) you
                                have used (and for which you have to pay) during the
                                period.</p>
                        </div>
                        
                        <div class="col-md-11">
                            <strong>What is meant by the terms AC, DC and
                                frequency?</strong>
                        </div>



                        <div class="col-md-11">
                            <p>Electricity is said to flow when electrons in a
                                suitable material (a "conductor") are induced to
                                move in a particular direction when a suitable force
                                (an "electromotive force" or EMF) is applied to the
                                material. This flow of electricity is called an
                                electrical current and is measured in terms of
                                amperes (usually shortened to amps). The EMF is
                                measured in terms of volts.</p>

                            <p>Direct Current (DC) electricity is the easiest to
                                visualise because here the electrons (the electrical
                                current) always move in the same direction. A
                                battery is the EMF source most commonly used to
                                produce small amounts of direct electrical current.
                            </p>

                            <p>For example, the common torch uses a battery as the
                                EMF source. Electrical current flows from one side
                                (e.g. the positive side) of the battery, through the
                                element in the torch bulb (in the process heating
                                the element to produce light) and completes the
                                circuit back to the other side (e.g. the negative
                                side) of the battery.</p>

                            <p>Alternating Current (AC) electricity can be thought
                                of as electricity that flows in one direction for a
                                short period of time, then reverses its direction of
                                flow for a short period of time, then reverses flow
                                again, and again, and again.. Why does it do this?
                                It's because the EMF source is not constant and
                                changes its polarity (positive and negative sides)
                                in a regular manner. The rate at which the
                                electrical current changes direction through a full
                                cycle (flows in one direction, changes direction and
                                flows in the opposite direction then changes back to
                                the original direction) is called its "frequency".
                            </p>
                        </div>
