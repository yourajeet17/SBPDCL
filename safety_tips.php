                                        <ul style="list-style-position: inside;">
                                            <li><b>Look for NRTL listings for your products and appliances. </b>NRTLs
                                                such as Underwriters Laboratories (UL), Edison Testing Laboratories
                                                (ETL) and Canadian Standards Association (CSA) test appliances to ensure
                                                they comply with OSHA’s electrical safety rules.<br>
                                                <br>
                                            </li>
                                            <li><b>Ask a professional to install electrical wiring. </b>Electrical
                                                systems are potentially dangerous. Even if you’re a skilled DIYer,
                                                consult with a professional before installing new wiring or electrical
                                                appliances. Licensed electricians have the skills and knowledge needed
                                                to keep your home safe and in compliance with electrical codes.<br>
                                                <br>
                                            </li>
                                            <li><b>Know what to do when the power goes down. </b>Knowing <a
                                                    href="http://blog.constellation.com/2016/05/27/brownout-vs-blackout-power-outage-tips/"
