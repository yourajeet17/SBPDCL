                                        <h5>3. Replace or repair damaged electrical cords to keep your home safe.</h5>
                                        <p>Damaged power cords are a serious residential electrical safety risk, and
                                            they are capable of causing both fires and electrocution. All power and
                                            extension cords should be checked regularly for signs of fraying and
                                            cracking, and they should then be repaired or replaced as needed. Power
                                            cords should not be stapled into place or run under rugs and furniture.
                                            Cords under rugs pose a tripping hazard and can overheat, while furniture
                                            can crush cord insulation and damage wires.</p>
                                        <p>The use of extension cords on a regular basis may mean that you don’t have
                                            enough outlets to fit your needs. Have a qualified electrician who
                                            understands electrical safety rules install additional outlets in rooms
                                            where you often use extension cords. When purchasing a power cord, consider
                                            the electrical load it will carry. A cord with a load of 16 AWG can handle
                                            up to 1,375 watts. For heavier loads, use a 14 or 12 AWG cord.</p>
                                        <p><em><strong>Pro tip: </strong>AWG stands for “American wire gauge.” The lower
                                                the number, the thicker the cord!</em><br>
                                            <br>
                                        </p>
                                        <h5>4. Keep your used and unused cords tidy and secure to prevent damage.</h5>
                                        <p>Electrical safety tips don’t just apply to power cords when they’re in
                                            use—cords also need to be stored safely to prevent damage. Keep stored cords
                                            away from children and pets (who may chew on or play with the cords). Try to
                                            avoid wrapping cords tightly around objects; this can stretch the cord or
                                            cause overheating. Never rest a cord on a hot surface in order to prevent
                                            damage to the cord’s insulation and wires.<br>
                                            &nbsp;</p>
                                        <h5>5. Unplug all your unused appliances to reduce potential risks.</h5>
                                        <p>One of the simplest electrical safety tips is also one of the easiest to
                                            forget: when an appliance is not in use, unplug it. Not only does this save
                                            you power by <a adhocenable="false"
                                                href="https://blog.constellation.com/2021/10/12/unplug-appliances-to-save-energy/"
                                                target="_blank" title="Opens in a new window"
                                                data-di-id="di-id-531aafb4-a110fce">reducing any phantom drain</a> (the
                                            amount of energy the device consumes even when not actively in use), but
                                            unplugging unused appliances also protects them from overheating or power
                                            surges.</p>
                                        <p>It’s often difficult to remember to unplug unused appliances, but the new
                                            generation of <a adhocenable="false"
                                                href="https://blog.constellation.com/2018/10/15/best-smart-plug-uses/"
                                                data-di-id="di-id-e148c413-6a63e961">smart plugs</a> offers a solution,
                                            allowing you to set power schedules for each outlet.<br>
                                            &nbsp;</p>
                                        <h5>6. Keep electrical devices and outlets away from water to prevent shock.
                                        </h5>
                                        <p>Water and electricity don’t mix well. To follow electrical safety rules, keep
                                            electrical equipment dry and away from water prevents damage to appliances
                                            and can protect against personal injury and electrocution. When working with
                                            electrical appliances, it’s important to have dry hands. Keeping electrical
                                            equipment away from plant pots, aquariums, sinks, showers and bathtubs
                                            lowers the risk of water and electricity coming into contact.<br>
                                            &nbsp;</p>
                                        <h5>7. Give your appliances proper space for air circulation to avoid
                                            overheating.</h5>
                                        <p>Without proper air circulation, electrical equipment can overheat and short
                                            out, and can become an electrical fire hazard. Make sure your appliances
                                            have proper air circulation, and avoid running electrical equipment in
                                            enclosed cabinets. For best electrical safety, it’s also important to store
                                            flammable objects well away from all appliances and electronics. Pay
                                            especially close attention to your <a adhocenable="false"
                                                href="https://blog.constellation.com/2016/01/27/which-is-more-energy-efficient-gas-or-electric-dryer/"
                                                data-di-id="di-id-d53bdbf0-4c980bf0">gas or electric dryer</a>, as these
                                            need to be situated at least a foot from the wall to function safely.<br>
                                            &nbsp;</p>
                                        <h5>8. Ensure that all your exhaust fans are clean to prevent fire hazards.</h5>
                                        <p>Some appliances have exhaust fans, which can get dirty or clogged with debris
                                            and make the appliance work harder. This can shorten the life of the
                                            appliance and can cause a risk to the home due to overheating, or even cause
                                            a buildup of dangerous gasses that can lead to an electrical fire hazard.
                                            Cleaning exhaust fans regularly helps prevent such hazards.<br>
                                            &nbsp;</p>
                                        <h5>9. Check that you’re using the correct wattage in all your fixtures and
                                            appliances.</h5>
                                        <p>Using the right bulbs can prevent electrical problems, so check all lamps,
                                            fixtures and appliances to ensure you’re using the correct wattage. If a
                                            light fixture has no wattage listed, use 60-watt bulbs or less. For unmarked
                                            ceiling fixtures, choose 25-watt bulbs.</p>
                                        <p><i><strong>Pro tip:</strong> LED bulbs consume less power and reduce the risk
                                                of fixtures overheating. Learn more about <a adhocenable="false"
                                                    href="https://blog.constellation.com/2016/03/25/led-vs-cfl-bulbs/"
                                                    target="_blank" title="Opens in a new window"
                                                    data-di-id="di-id-15dca147-e94c7d47">LED light benefits</a>.</i></p>
                                        <h5>10. Be aware of heaters and water heaters to prevent potential accidents.
                                        </h5>
                                        <p>Combustible items should be kept away from <a adhocenable="false"
                                                href="http://blog.constellation.com/2016/12/09/choosing-most-efficient-space-heater/"
                                                data-di-id="di-id-49353792-4e50165">portable heaters</a> and built-in
                                            furnaces. For furnace safety, store combustibles far away from any heating
                                            appliances. Portable heaters should not be operated close to drapes, and to
                                            prevent tipping, they should only ever be placed on a stable surface.</p>
