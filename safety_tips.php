                                                    out</a> helps you protect yourself from downed power lines and other
                                                hazards while shielding your appliances from damage caused by power
                                                surges. Check out <a
                                                    href="https://www.constellation.com/energy-101/report-a-power-outage.html"
                                                    data-di-id="di-id-80c0e06c-ac332f3e">how to report a power
                                                    outage</a> to keep you and your family safe.<br>
                                                <br>
                                            </li>
                                            <li><b>Install smoke detectors. </b>Electrical fires often smolder before
                                                breaking out into open flame, and the <a
                                                    href="https://www.usfa.fema.gov/prevention/outreach/electrical.html"
                                                    data-di-id="di-id-464a5000-63796b46">U.S. Fire Administration</a>
                                                reports that most electrical fires occur between midnight and 6:00 a.m.
                                                <a href="https://blog.constellation.com/2018/09/19/best-smart-smoke-detectors/"
                                                    data-di-id="di-id-288d120e-943b97cf">Installing smoke alarms</a>
                                                helps alert you to the dangers of electrical fires.<br>
                                                <br>
                                            </li>
                                            <li><b>Run a generator the safe way. </b>A generator can keep the lights on
